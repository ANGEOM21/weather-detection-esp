// --- Setup ---
void setup()
{
  Serial.begin(115200);
  Serial.println("ðŸ”¥ ESP8266 Ready");
  // Inisialisasi filesystem dan WiFi
  LittleFS.begin();
  setup_wifi();

  // Inisialisasi MQTT dan sensor
  if (WiFi.status() == WL_CONNECTED)
  {
    sync_time();
    init_mqtt(client, mqtt_server, secureClient, mqtt_root_ca);
    init_error_module(client);
    ensure_mqtt_connected(client, mqtt_user, mqtt_pass);
    init_sensors();
  }
  else
  {
    Serial.println("Wifi Not Connected");
  }
}

// --- Loop ---
void loop()
{
  // Loop AP Mode Handle To Connection WIFI
  handle_ap_mode();

  if (WiFi.status() == WL_CONNECTED)
  {
    if (!client->connected())
    {
      ensure_mqtt_connected(client, mqtt_user, mqtt_pass);
    }
    client->loop();

    // Loop Heartbeat
    static unsigned long lastHeartbeat = 0;
    if (millis() - lastHeartbeat > 60000)
    { // setiap 1 menit
      lastHeartbeat = millis();
      heartbeat_status();
    }

    // Loop Sensor
    static unsigned long lastSend = 0;
    if (millis() - lastSend > 5000)
    {
      lastSend = millis();
      publish_data(client, lastPressure);
    }
  }
}
